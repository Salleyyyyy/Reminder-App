package com.server.model;

/**
 * This class contains time intervals in milliseconds.
 */
public class IntervalUnit {

	static final long INTERVAL_HOUR = 1000L * 60L * 60L * 1L;

	static final long INTERVAL_HALF_AN_HOUR = INTERVAL_HOUR / 2;

	static final long INTERVAL_DAY = INTERVAL_HOUR * 24L;

}
